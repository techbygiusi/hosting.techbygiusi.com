function preserveWhitespace(source, translated) {
  const leading = source.match(/^\s*/)?.[0] || '';
  const trailing = source.match(/\s*$/)?.[0] || '';
  return `${leading}${translated}${trailing}`;
}

export function translatePortalText(value, language) {
  if (value === null || value === undefined) return value;
  const source = String(value);
  const trimmed = source.trim().replace(/\s+/g, ' ');
  if (!trimmed) return source;
  const map = language === 'de' ? EN_DE_PHRASES : DE_EN_PHRASES;
  if (map[trimmed]) return preserveWhitespace(source, map[trimmed]);
  for (const [dePattern, enReplacement, enPattern, deReplacement] of dynamicPatterns) {
    const pattern = language === 'de' ? enPattern : dePattern;
    const replacement = language === 'de' ? deReplacement : enReplacement;
    if (pattern.test(trimmed)) return preserveWhitespace(source, trimmed.replace(pattern, replacement));
  }
  return source;
}

function translateNode(node, language) {
  if (node.nodeType === Node.TEXT_NODE) {
    const current = node.nodeValue || '';
    if (!originalText.has(node) || (lastAppliedText.has(node) && current !== lastAppliedText.get(node))) {
      originalText.set(node, current);
    }
    const base = originalText.get(node);
    const next = translatePortalText(base, language);
    lastAppliedText.set(node, next);
    if (current !== next) node.nodeValue = next;
    return;
  }
  if (node.nodeType !== Node.ELEMENT_NODE) return;
  const element = node;
  if (element.matches('script, style, code, pre')) return;
  const attrs = ['placeholder', 'title', 'aria-label', 'alt'];
  if (!originalAttributes.has(element)) originalAttributes.set(element, {});
  if (!lastAppliedAttributes.has(element)) lastAppliedAttributes.set(element, {});
  const saved = originalAttributes.get(element);
  const applied = lastAppliedAttributes.get(element);
  for (const attr of attrs) {
    if (!element.hasAttribute(attr)) continue;
    const current = element.getAttribute(attr) || '';
    if (!(attr in saved) || (attr in applied && current !== applied[attr])) saved[attr] = current;
    const next = translatePortalText(saved[attr], language);
    applied[attr] = next;
    if (current !== next) element.setAttribute(attr, next);
  }
  if (element.matches('textarea')) return;
  for (const child of element.childNodes) translateNode(child, language);
}

export function applyPortalLanguage(language = readStoredLanguage()) {
  if (typeof document === 'undefined' || !document.body || applying) return;
  applying = true;
  try {
    document.documentElement.lang = language;
    translateNode(document.body, language);
  } finally {
    applying = false;
  }
}

